/**
 * Example: Basic Usage
 */

// Encode a file
// bun run encode input.txt output

// Decode a file
// bun run decode output result.txt

// Encode a directory
// bun run encode my-folder output

// Decode and extract directory
// bun run decode output recovered-folder

// With compression
// bun run encode input.txt output --compress

// Custom video settings
// bun run encode input.txt output --fps 60 --frame 3840x2160
